// Declare a boolean variable indicating if today is a weekday
let isWeekday = true;

// Print the value of the variable
console.log("Is today a weekday?", isWeekday);

// Change the variable to its opposite value
isWeekday = !isWeekday; // Use the NOT operator to toggle the value
console.log("After changing, is today a weekday?", isWeekday);

// Simple Conversion Program: Convert Celsius to Fahrenheit
const celsius = 25; // Example Celsius temperature
const fahrenheit = (celsius * 9/5) + 32; // Conversion formula
console.log(celsius + "°C is equal to " + fahrenheit + "°F.");
