// Declare variables of different data types
const myS = "Hello, JavaScript!"; // String type
const myN = 42;                  // Number type
const myB = true;               // Boolean type

// Print each variable and its type using typeof
console.log("Value:", myS, "| Type:", typeof myS);
console.log("Value:", myN, "| Type:", typeof myN);
console.log("Value:", myB, "| Type:", typeof myB);
