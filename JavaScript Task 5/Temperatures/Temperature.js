// Step 1: Declare a variable for temperature in Celsius
const celsius = 25; // Example temperature in Celsius

// Step 2: Convert the temperature to Fahrenheit
const fahrenheit = (celsius * 9/5) + 32; // Conversion formula

// Step 3: Print both the Celsius and Fahrenheit temperatures
console.log("Temperature in Celsius:", celsius + "°C");
console.log("Temperature in Fahrenheit:", fahrenheit + "°F");
